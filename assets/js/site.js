function resolveTranslationValue(source, path) {
  return path.split(".").reduce((current, part) => {
    if (current && Object.prototype.hasOwnProperty.call(current, part)) {
      return current[part];
    }

    return undefined;
  }, source);
}

function applyHomeTranslations(language) {
  const selectedLanguage = homeTranslations[language] ? language : "en";
  const translations = homeTranslations[selectedLanguage];

  document.documentElement.lang = selectedLanguage;
  document.title = translations.seo.title;

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute("content", translations.seo.description);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = resolveTranslationValue(translations, element.dataset.i18n);
    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    element.dataset.i18nAttr.split(";").forEach((pair) => {
      const [attributeName, translationPath] = pair.split(":");
      const value = resolveTranslationValue(translations, translationPath);

      if (attributeName && typeof value === "string") {
        element.setAttribute(attributeName, value);
      }
    });
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.lang === selectedLanguage));
  });

  try {
    localStorage.setItem("preferred-language", selectedLanguage);
  } catch (error) {
    console.warn("Could not store preferred language.", error);
  }
}

function initHomeLanguageSwitcher() {
  if (document.body.dataset.page !== "home") {
    return;
  }

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      applyHomeTranslations(button.dataset.lang);
    });
  });

  let initialLanguage = "en";

  try {
    const storedLanguage = localStorage.getItem("preferred-language");
    if (storedLanguage) {
      initialLanguage = storedLanguage;
    } else if (navigator.language) {
      const browserLanguage = navigator.language.toLowerCase().split("-")[0];
      if (homeTranslations[browserLanguage]) {
        initialLanguage = browserLanguage;
      }
    }
  } catch (error) {
    console.warn("Could not read preferred language.", error);
  }

  applyHomeTranslations(initialLanguage);
}

function setCurrentYear() {
  const year = new Date().getFullYear();
  document.querySelectorAll("[data-current-year]").forEach((element) => {
    element.textContent = String(year);
  });
}

document.addEventListener("DOMContentLoaded", () => {
  setCurrentYear();
  initHomeLanguageSwitcher();
});
